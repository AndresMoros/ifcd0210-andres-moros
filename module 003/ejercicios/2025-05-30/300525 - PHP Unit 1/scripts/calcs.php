<?php

// Sum function
function sum($n1, $n2)
{
    return $n1 + $n2;
}

// Substract function
function substrac($n1, $n2) 
{
    return $n1 - $n2;
}

// Multiplication function
function mult($n1, $n2) 
{
    return $n1 * $n2;
}

// Division function
function div($n1, $n2) 
{
    return $n1 / $n2;
}

?>