using System;


    class IncrementoDecremento
    {
        static void Main(string[] args)
        {
            int x;
						
			x = 5;
			
			
				
			Console.WriteLine(x);
			Console.WriteLine(x++);
			Console.WriteLine(x);
			
			Console.WriteLine(x);
			Console.WriteLine(++x);
			Console.WriteLine(x);
			
			Console.WriteLine(x);
			Console.WriteLine(x--);
			Console.WriteLine(x);
			
			Console.WriteLine(x);
			Console.WriteLine(--x);
			Console.WriteLine(x);
			
			            
            Console.ReadKey();
        }
    }